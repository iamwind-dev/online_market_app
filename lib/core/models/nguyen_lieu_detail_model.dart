/// Model cho chi tiết nguyên liệu
class NguyenLieuDetailModel {
  final String maNguyenLieu;
  final String tenNguyenLieu;
  final String? donVi;
  final String maNhomNguyenLieu;
  final String tenNhomNguyenLieu;
  final int soGianHang;
  final double? giaGoc;
  final String? giaCuoi;
  final String? ngayCapNhatMoiNhat;
  final String? hinhAnhMoiNhat;

  NguyenLieuDetailModel({
    required this.maNguyenLieu,
    required this.tenNguyenLieu,
    this.donVi,
    required this.maNhomNguyenLieu,
    required this.tenNhomNguyenLieu,
    required this.soGianHang,
    this.giaGoc,
    this.giaCuoi,
    this.ngayCapNhatMoiNhat,
    this.hinhAnhMoiNhat,
  });

  factory NguyenLieuDetailModel.fromJson(Map<String, dynamic> json) {
    return NguyenLieuDetailModel(
      maNguyenLieu: json['ma_nguyen_lieu'] as String,
      tenNguyenLieu: json['ten_nguyen_lieu'] as String,
      donVi: json['don_vi'] as String?,
      maNhomNguyenLieu: json['ma_nhom_nguyen_lieu'] as String,
      tenNhomNguyenLieu: json['ten_nhom_nguyen_lieu'] as String,
      soGianHang: (json['so_gian_hang'] as num).toInt(),
      giaGoc: json['gia_goc'] != null ? (json['gia_goc'] as num).toDouble() : null,
      giaCuoi: json['gia_cuoi']?.toString(),
      ngayCapNhatMoiNhat: json['ngay_cap_nhat_moi_nhat'] as String?,
      hinhAnhMoiNhat: json['hinh_anh_moi_nhat'] as String?,
    );
  }
}

/// Model cho người bán (seller)
class SellerModel {
  final String maGianHang;
  final String tenGianHang;
  final String viTri;
  final String maCho;
  final double? giaGoc;
  final String? giaCuoi;
  final String? hinhAnh;
  final String? ngayCapNhat;
  final int soLuongBan;
  final String? donViBan;

  SellerModel({
    required this.maGianHang,
    required this.tenGianHang,
    required this.viTri,
    required this.maCho,
    this.giaGoc,
    this.giaCuoi,
    this.hinhAnh,
    this.ngayCapNhat,
    required this.soLuongBan,
    this.donViBan,
  });

  factory SellerModel.fromJson(Map<String, dynamic> json) {
    return SellerModel(
      maGianHang: json['ma_gian_hang'] as String,
      tenGianHang: json['ten_gian_hang'] as String,
      viTri: json['vi_tri'] as String,
      maCho: json['ma_cho'] as String,
      giaGoc: json['gia_goc'] != null ? (json['gia_goc'] as num).toDouble() : null,
      giaCuoi: json['gia_cuoi']?.toString(),
      hinhAnh: json['hinh_anh'] as String?,
      ngayCapNhat: json['ngay_cap_nhat'] as String?,
      soLuongBan: (json['so_luong_ban'] as num).toInt(),
      donViBan: json['don_vi_ban'] as String?,
    );
  }
}

/// Response model cho chi tiết nguyên liệu
class NguyenLieuDetailResponse {
  final bool success;
  final NguyenLieuDetailModel detail;
  final SellersData sellers;

  NguyenLieuDetailResponse({
    required this.success,
    required this.detail,
    required this.sellers,
  });

  factory NguyenLieuDetailResponse.fromJson(Map<String, dynamic> json) {
    return NguyenLieuDetailResponse(
      success: json['success'] as bool,
      detail: NguyenLieuDetailModel.fromJson(json['detail'] as Map<String, dynamic>),
      sellers: SellersData.fromJson(json['sellers'] as Map<String, dynamic>),
    );
  }
}

/// Sellers data với pagination
class SellersData {
  final List<SellerModel> data;
  final SellersMeta meta;

  SellersData({
    required this.data,
    required this.meta,
  });

  factory SellersData.fromJson(Map<String, dynamic> json) {
    return SellersData(
      data: (json['data'] as List<dynamic>)
          .map((item) => SellerModel.fromJson(item as Map<String, dynamic>))
          .toList(),
      meta: SellersMeta.fromJson(json['meta'] as Map<String, dynamic>),
    );
  }
}

/// Meta information cho sellers pagination
class SellersMeta {
  final int page;
  final int limit;
  final int total;
  final bool hasNext;

  SellersMeta({
    required this.page,
    required this.limit,
    required this.total,
    required this.hasNext,
  });

  factory SellersMeta.fromJson(Map<String, dynamic> json) {
    return SellersMeta(
      page: (json['page'] as num).toInt(),
      limit: (json['limit'] as num).toInt(),
      total: (json['total'] as num).toInt(),
      hasNext: json['hasNext'] as bool,
    );
  }
}
